package com.vulnverify.core.entity;

/**
 * 
 * @author LiWenbin
 *
 */
public class PageInfo {
	
	private int index;
	private int count;
	private int size;
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
}
